# Variables are dynamically typed

n = 0
print("n = ", n)

m = "abc"
print("n = ", m)

# Multiple Assignments

a, b, c = 0.125, "string", False
print("a = ", a)
print("b = ", b)
print("c = ", c)


# Increment Operation

n = n + 1
n += 1
# n++    # Not Valid

print("n = ", n)

# None is Null
p = None
print("p = ", p)
